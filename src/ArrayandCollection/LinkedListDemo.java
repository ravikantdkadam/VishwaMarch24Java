package ArrayandCollection;

import java.util.LinkedList;
import java.util.Vector;

public class LinkedListDemo {

	public static void main(String[] args) {
		
		
		LinkedList <String > link = new LinkedList<String>();
		
		
		
		link.add("Vishwatech");
		link.add("Solutions");
		link.add("1234");
		link.add("Vishwatech@123.com");
		link.addFirst("Start");
		link.addLast("final");
		
		
		System.out.println(link);
		
		System.out.println(link.size());
		
		
		// we can use same methods which we used in arraylist 
		
		
		
		Vector<Integer> vt =new Vector<Integer>();  // we are not using vector
		
		vt.add(1223);
		
		// we can use same methods which we used in arraylist 
		
		
		
	}

}
