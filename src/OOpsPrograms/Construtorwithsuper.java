package OOpsPrograms;

public class Construtorwithsuper {
	

public Construtorwithsuper() {
	
	
System.out.println("This is default parent constructor");

}


public Construtorwithsuper(int age ) {
	
	
System.out.println("This is single parameter parent constructor"+age);

}


public Construtorwithsuper(int a,int b ) {
	
	
System.out.println("This is double parameter parent constructor"+(a+b));

}



}