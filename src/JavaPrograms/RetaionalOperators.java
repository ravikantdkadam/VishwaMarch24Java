package JavaPrograms;

public class RetaionalOperators {    

	public static void main(String[] args) {
		int x=3;
		
		int y=5;
		
		
		System.out.println(x==y);   // Return false because 3 not equal to 5
		
		
		System.out.println(x!=y);   //   Return true because 3 not equal to 5
		
		System.out.println(x>y);    // Return false because 3 not grater than to 5
		
		System.out.println(x<y);    //   Return true because 3 less than 5
		
		System.out.println(x>=y);   //   Return false because 3 not grater than or equal to 5
		
		System.out.println(x<=y);   //   Return true because 3 less than 5 but not equal to 5
		
	}

}
