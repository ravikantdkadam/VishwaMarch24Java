package JavaPrograms;

public class LogicalOperators {

	public static void main(String[] args) {


		int x=5;
		
		
		System.out.println(x>3 && x<10);     // And Operator=====true
		
		
		System.out.println(x>3 || x<2);     // Or Operator ====true
		
		
		System.out.println(!(x>10));        // NOT operator ====reverse result

	}

}
