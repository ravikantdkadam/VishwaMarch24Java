package JavaPrograms;

public class Incrementalanddecremental {

	public static void main(String[] args) {
		
		
		int i=1;
		
		int j;
		

		j=i++;  // post increment 
		
		
		//incremental operators : pre increment and post increment 
		
		//decremental Operators: pre decrement and post decrement 

		
		
		//Post increment: Here First print the value of variable and then it will increase by 1 
		
		
		System.out.println("The value of i is "+i);  //2
		
		System.out.println("The value of j is "+j);   //1
		
		
	System.out.println("=====================================================================================================");
		
		
		int m=1;
		int n;
		
	
	    n=m--;   // post decrement 
	
	//Post decrement: Here First print the value of variable and then it will decreased by 1 
	
	
			System.out.println("The value of m is "+m);  //0
			
			System.out.println("The value of n is "+n);   //1  
			
			
			
		System.out.println("==========================================================================================================================");	
			
			

		int x=1;
		
		int y;
		

		y=++x;  // pre increment 
		
	
		//Pre increment: Here first value of variable is increased by 1 and then it will print 
		
		
		System.out.println("The value of x is "+x);  // 2
		
		System.out.println("The value of y is "+y);   //2
		
		
		System.out.println("==========================================================================================================================");
		
		
		
		int e=1;
		
		int f;
		

		f=--e;  // pre decrement 
		
	
		//Pre decrement: Here first value of variable is decreased by 1 and then it will print 
		
		
		System.out.println("The value of e is "+e);  // 0
		
		System.out.println("The value of f is "+f);   //0
		
		
			
		}
	
	
	

}
