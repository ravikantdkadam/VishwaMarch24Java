package JavaPrograms;

public class ArithmaticOperators {

	public static void main(String[] args) {
		
		int x=7;
		
		int y=5;
		
		System.out.println("Addition Operator"+(x+y));
		
		System.out.println("Substraction Operator"+(x-y));
		
		System.out.println("Multiplication Operator"+(x*y));
		
		System.out.println("Division Operator"+(x/y));   // Divide one value by another
		
		System.out.println("modulus Operator"+(x%y));   // Return division reminder

	}

}
