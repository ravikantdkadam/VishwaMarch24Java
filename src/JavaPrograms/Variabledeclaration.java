package JavaPrograms;

public class Variabledeclaration {

	public static void main(String[] args) {


		//Variable is piece of memory to store the information or data 
		
		
		
		String batchname;          // Variable Declaration 
		
		
		 batchname= "March2024";    // Variable Initialization 
		
		
		System.out.println(batchname);     // Variable Usage
		
		
		
		
		
	System.out.println("====================================================");
	
	
	
		
		int a;          // Variable Declaration 
		
		a=123;            // Variable Initialization 
		
		
		System.out.println("The value of a is "+a);     // Variable Usage     
		
		
	}

}
